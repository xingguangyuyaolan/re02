doc.add_heading('3.5 混合网络设计', level=2)

add_paragraph(
    '混合网络（Mixing Network）是QMIX架构中将个体智能体的局部Q值聚合为团队总Q值的核心组件。'
    '为了支持消融实验（对比不同值分解方法和不同注意力机制的独立效果），本课题实现了三种混合网络变体：VDN（加性分解）、'
    '标准QMIX（超网络+单调性约束）和StateAttentionQMixNet（在QMIX基础上加入全局状态注意力模块）。'
    '三种混合网络共享相同的调用接口（forward(q, s)，接收个体Q值矩阵q ∈ R^{B×T×N} 和全局状态s ∈ R^{B×T×|S|}），'
    '使得在不同实验配置之间切换混合网络类型仅需修改一行配置参数。'
)

doc.add_heading('3.5.1 VDN：加性分解', level=3)

add_paragraph(
    'VDN（Value Decomposition Networks）[7]混合网络是最简值分解的基准实现。其混合函数为：'
    'Q_tot(s, a) = Σ_{i=1}^{N} Q_i(τ_i, a_i)。'
    'VDN不包含任何可学习参数，不对全局状态s做任何利用，也不对个体Q值之间的关系施加额外的结构约束。'
    '在本课题的消融实验中，VDN作为"无混合网络学习能力"的基线参考点。'
    '如果某个更复杂的混合网络方案未能显著超越VDN的覆盖性能，则表明复杂方案的附加表示能力在该任务上并未发挥实效。'
)

doc.add_heading('3.5.2 标准QMIX混合网络', level=3)

add_paragraph(
    '标准QMIX混合网络[8]通过超网络（HyperNetwork）和绝对值激活函数保证了混合网络的单调性，使Q_tot是每个Q_i的单调递增函数。'
    '混合网络的两层结构如下：第一层将N个智能体的个体Q值通过权重矩阵W1 ∈ R^{N×d_mix} 映射到d_mix维的隐藏空间，'
    '加上偏置b1后通过ELU激活函数；第二层将隐藏表示通过权重矩阵W2 ∈ R^{d_mix×1} 映射为标量Q_total。'
    '两个权重矩阵W1、W2和偏置向量b1、b2均由超网络根据全局状态s动态生成。'
)

add_paragraph(
    '具体地，对于第一层的权重W1，其形状为 N × d_mix，共 N·d_mix 个标量值。超网络hyper_w1是一个以s为输入的前馈神经网络，'
    '输出维度为 N·d_mix，经过reshape得到W1，最后取绝对值（W1 = |W1|）以确保非负性。偏置b1（d_mix维）同样由一个超网络hyper_b1'
    '从s生成，但不取绝对值（偏置的非负性不影响argmax等价性）。类似地，W2（d_mix × 1）和b2（标量）由各自的超网络从s生成，'
    'W2同样取绝对值。这种"超网络生成权重+绝对值保证单调性"的设计是QMIX保证IGM条件的核心技术手段。'
)

add_paragraph(
    '在本课题的默认配置中，QMIX混合网络的隐藏维度 d_mix = 64，超网络的隐藏层维度为128，超网络采用单层线性变换'
    '（hyper_layers_num=1）以减少参数总量。实验中发现，双层的超网络（hyper_layers_num=2）在该任务上相较于单层超网络'
    '并未表现出统计显著的性能优势，而参数量的增加（约翻倍）却明显拖慢了训练速度。'
)

doc.add_heading('3.5.3 状态注意力混合网络', level=3)

add_paragraph(
    '标准QMIX混合网络的一个隐含假设是，所有智能体在混合过程中的"角色"是对等的——混合权重W1虽然由全局状态s通过超网络动态生成，'
    '但超网络对每个Q_i的权重生成方式在结构上是同质的。然而在多无人机覆盖任务的不同阶段，不同智能体对全局覆盖目标的实际贡献'
    '确实是不对等的。例如，在任务早期，距离未覆盖区域较近的无人机应被混合网络赋予更大的权重，因为它的动作选择对团队覆盖进度的影响'
    '更为直接；在任务中后期，当绝大多数区域已完成覆盖时，那些处于尚未覆盖的缺口位置附近的无人机对最终完成覆盖具有决定性的作用。'
)

add_paragraph(
    'StateAttentionQMixNet在标准QMIX混合网络的基础上增加了一个全局状态注意力模块，该模块位于个体Q值被送入两层混合网络之前。'
    '其计算过程为：首先，全局状态s经过一个两层的神经网络 attention_net(s): R^{|S|} → R^{N}，输出N维的未归一化重要性分数。'
    '然后，对这N个分数进行Softmax归一化并乘以N（以确保均值约为1，避免整体缩放效应）：'
    'importance_i = N · softmax_i(attention_net(s))。'
    '最后，将每个智能体的个体Q值乘以对应的重要性系数：Q\'_i = Q_i · importance_i。'
    '经过重要性加权后的Q\'_i随后进入与标准QMIX完全相同的两层混合网络进行计算。'
)

add_paragraph(
    '需要特别强调，重要性系数是通过Softmax产生的，因此严格为正。在正重要性系数与标准QMIX混合网络的非负权重相乘后，'
    '整个网络依然满足 ∂Q_tot/∂Q_i ≥ 0 的单调性条件，因此StateAttentionQMixNet仍然保证IGM原则成立——'
    '团队的最优联合动作与各智能体的独立贪心动作选择保持一致。换句话说，全局状态注意力的引入扩展了混合网络的表示能力，'
    '但并未以破坏CTDE框架的理论基础为代价。'
)

