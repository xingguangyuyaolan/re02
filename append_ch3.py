doc.add_heading('3.2 网络架构总览', level=2)

add_paragraph(
    '本文提出的方法在QMIX算法框架的基础上，系统性地引入了三类注意力机制，分别作用于智能体个体策略网络内部'
    '（观测自注意力）、多个智能体策略网络之间（跨智能体图注意力）以及混合网络层级（全局状态注意力）。'
    '算法的整体架构严格遵循CTDE（集中训练分散执行）范式设计，分为集中训练阶段和分散执行阶段两条路径。'
)

add_paragraph(
    '在集中训练阶段，算法的计算流程如下。首先，每架无人机的局部观测向量 o_i 经过观测预处理模块中的位姿归一化、激光雷达裁剪与缩放到[0,1]'
    '等标准化操作后，与"上一时间步执行的动作"的One-Hot编码（11维）以及该智能体自身的ID编码（4维One-Hot）在特征维度上拼接，'
    '形成116维的完整输入向量。随后，该输入流经一个全连接层（fc1）映射到128维的特征空间，经过ReLU激活后送入单层GRU循环网络。'
    'GRU将该时刻的特征与内部维护的历史隐状态进行融合，输出128维的隐状态向量 h_i^t，该向量编码了智能体截至时间步t的全部观测历史信息。'
)

add_paragraph(
    '接着，隐状态 h_i^t 进入观测自注意力模块（ObservationSelfAttention，详见3.3.3节）。该模块将128维隐状态分割为4个32维的'
    '特征token，通过4头多头自注意力在token维度上进行特征重标定，输出增强后的隐状态 h\'_i^t。'
    '然后，所有N架无人机的增强隐状态 {h\'_i^t} 被汇集到跨智能体图注意力模块（CrossAgentGAT，详见3.4节），'
    '该模块将每架无人机视为完全图中的一个节点，通过图注意力运算输出融合了队友信息的协作隐状态 h\'\'_i^t。'
    '最后，协作隐状态通过一个全连接输出层（fc2）生成11维的个体Q值向量 Q_i(o_i, a_i)。'
)

add_paragraph(
    '在获得所有智能体的个体Q值 {Q_i} 和全局状态s之后，混合网络（详见3.5节）将它们映射为一个标量团队Q_total。'
    '根据算法配置的不同，混合网络可以是VDN（简单求和）、标准QMIX（超网络+单调性约束）或StateAttentionQMixNet'
    '（在QMIX基础上增加全局状态注意力模块）。之后，以TD-error最小化为目标对整个网络的参数进行端到端梯度下降优化。'
)

add_paragraph(
    '在分散执行阶段，智能体网络的运行路径与训练阶段保持一致——从局部观测出发，依次经过输入拼接、全连接层、GRU、'
    '自注意力和跨智能体注意力到达Q值输出——唯一的不同在于混合网络被完全移除（因为它需要全局状态s作为输入，'
    '而全局状态在分布式执行中不可获得）。每个智能体独立地在自身的Q值向量上执行贪心选择（argmax_i Q_i），'
    '得到离散动作索引后查表转换为四维连续速度指令，发送至无人机底层控制器。'
)

