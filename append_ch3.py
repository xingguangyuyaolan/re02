doc.add_heading('3.4 跨智能体图注意力网络', level=2)

add_paragraph(
    '在多无人机覆盖任务中，一个决定团队效率的关键因素是无人机之间的空间分工质量。理想情况下，四架无人机应该自发地分散到'
    '目标区域的不同象限，各自负责覆盖约四分之一的面积，并在各自的子区域内以高效的模式遍历格子。'
    '然而，标准的QMIX框架并不提供任何显式的智能体间信息交互机制——在训练阶段，各智能体通过混合网络以隐式的方式'
    '共享信息；在执行阶段，智能体之间彼此完全独立决策。这在实践中可能导致以下问题：两架或多架无人机同时被相似的观测输入触发，'
    '做出相同的运动选择，从而飞向同一个区域、反复覆盖相同的格子，造成覆盖效率的大幅降低。'
)

doc.add_heading('3.4.1 CrossAgentGAT的结构设计', level=3)

add_paragraph(
    '为解决上述问题，本课题设计了跨智能体图注意力网络（CrossAgentGAT），放置在所有智能体的GRU+自注意力输出之后、'
    'Q值输出层fc2之前。CrossAgentGAT将N=4架无人机建模为完全图 G=(V, E) 中的节点：每个节点v_i的特征为其对应的隐状态h_i ∈ R^{128}'
    '（如果启用了自注意力，则为经过自注意力增强后的隐状态）；所有节点之间均存在双向边，即每架无人机都可能关注任何其他无人机。'
    '这种完全图的设计意味着算法不预设任何通信拓扑的限制，而是让注意力机制从数据中自主学习哪些智能体之间最值得彼此关注。'
)

add_paragraph(
    'CrossAgentGAT沿用了GAT[5]的注意力计算范式，但采用了标准的多头缩放点积注意力（而非GAT中原始的加性注意力机制）以与'
    'Transformer架构保持一致的计算效率优势。设节点i的输入特征为 h_i ∈ R^{128}，对于每个注意力头，首先通过三个独立的线性投影矩阵'
    'W_q, W_k, W_v ∈ R^{128×32} 分别生成查询、键和值向量（4头注意力下每个头的维度为32）。'
    '节点i对节点j的注意力权重计算为：α_{ij} = softmax_j( (Q_i · K_j^T) / √32 )。'
    '该权重度量了"无人机i在决定自身动作时应该在多大程度上考虑无人机j的状态"。节点i经过注意力聚合后的输出为：'
    'h\'_i = W_out · Concat( Σ_j α_{ij}^(1) V_j^(1), ..., Σ_j α_{ij}^(4) V_j^(4) )。'
    '最后通过残差连接和LayerNorm：h\'\'_i = LayerNorm(h_i + h\'_i)。'
)

add_paragraph(
    '残差连接的保留对于训练稳定性至关重要，因为它在极端情况下允许GAT层退化为恒等映射——如果跨智能体注意力的输出对任务无益'
    '（例如在完全空白的开放空间中智能体之间的协作需求确实较弱），梯度自然会驱使注意力权重趋向于均匀分布，'
    '此时残差连接保证了h\'\'_i ≈ h_i，网络的其余部分不会受到干扰。'
)

doc.add_heading('3.4.2 稳定化训练策略', level=3)

add_paragraph(
    '在CTDE框架下训练跨智能体注意力面临一个特定的不稳定性问题。训练时，所有智能体的策略网络是同步更新的——这意味着在一次梯度'
    '更新中，不仅当前智能体i的策略发生了变化，所有"队友"智能体j的策略也同样发生了变化。对节点i而言，GAT中来自其他节点j的'
    '键和值向量既是当前关注的依据，又会在同一次参数更新后发生改变。这种"移动目标"效应（近似于同时训练一个编码器和一个解码器'
    '而没有采用交替优化）在训练早期可能导致梯度估计的高方差和学习信号的不稳定。'
)

add_paragraph(
    '本课题借鉴了近年来在表示学习中广泛使用的stop-gradient策略来缓解该问题。具体做法如下：'
    '在计算节点i对节点j的注意力时，对于来自其他节点j（j ≠ i）的键向量K_j和值向量V_j，在其前向传播路径上执行梯度截断（detach操作），'
    '即这些向量参与注意力分布的计算和加权聚合，但不将梯度回传至产生它们的GRU网络参数。'
    '只有节点i自身的查询向量Q_i以及所有注意力权重计算中的可学习投影矩阵W_q、W_k、W_v和W_out接受梯度更新。'
    '这一操作的效果相当于将其他节点在当前时间步的隐状态视为"固定的上下文信息"，从而为节点i的注意力学习提供了一个稳定的目标。'
    '在后续的梯度更新中，随着所有节点的策略参数同步改善，"固定的上下文"的质量也会因此提升。'
    '实践表明，该策略在几乎不增加计算开销的前提下，显著降低了训练早期的损失振荡幅度，并加快了覆盖性能指标的收敛速度。'
)

