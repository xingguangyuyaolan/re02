doc.add_heading('2.3.3 注意力机制在MARL中的三种作用层次', level=3)

add_paragraph(
    '综合现有文献[24]和本课题的研究设计，可以将注意力机制在多智能体强化学习中的引入位置归纳为三个层次。'
)

add_paragraph(
    '第一层次是观测自注意力（Observation Self-Attention），作用于单个智能体策略网络的内部。智能体在每个时间步接收到的高维'
    '观测向量中往往混杂了大量冗余或弱相关的环境信息——例如36维的激光雷达读数中大多数方向可能并无障碍物。'
    '观测自注意力使策略网络能够自动学习将有限的表示容量集中在观测中信息密度最高的区域，从而提升特征提取的效率。'
    '本课题将此机制实现为GRU隐状态的token化分割和多头自注意力，使网络能够在不同的"注意力头"中分别关注空间位置、'
    '障碍物距离、覆盖进度等不同语义维度的观测子空间。'
)

add_paragraph(
    '第二层次是跨智能体注意力（Cross-Agent Attention），作用于多个智能体策略网络之间的信息交互。在多无人机覆盖任务中，'
    '一个至关重要的协作需求是避免多架无人机同时向同一区域飞行——即减少覆盖重叠。跨智能体注意力通过计算智能体之间隐状态的'
    '相关性，使每架无人机在执行动作选择之前"意识"到其他队友的当前位置、运动方向和覆盖意图，从而在个体决策中自然地融入'
    '避让和分工的考量。与显式的通信协议相比，这种基于注意力的隐式协作不需要额外的通信带宽和同步机制，具有更好的可扩展性。'
)

add_paragraph(
    '第三层次是混合注意力（Mixing Attention），作用于将个体Q值融合为团队Q_total的混合网络层级。标准QMIX的混合网络使用'
    '超网络从全局状态中生成固定的混合权重，对所有智能体在所有时刻均使用相同的混合架构。混合注意力则允许混合网络根据'
    '全局覆盖态势动态评估各智能体的重要性——例如，在覆盖任务初期，距离未覆盖区域较近的无人机应获得更高的混合权重；'
    '在任务接近完成时，那些处于关键缺口位置的无人机应该获得更高的权重。StateAttentionQMixNet正是通过对全局状态进行注意力'
    '编码并将生成的动态重要性系数乘入各智能体的Q值的方式来实现这一机制的。'
)

