    '这种"训练时信息丰富、执行时信息受限"的设计既可以最大化训练阶段的样本效率，又保证了策略在实际部署中的可行性和鲁棒性。'
    'QMIX及其变体算法均为CTDE范式的典型代表：训练时混合网络以全局状态为输入进行集中式的值函数分解，执行时各智能体的策略网络'
