    '任意数量的其他节点可以订阅该Topic以异步接收消息，这种多对多的松耦合通信模式非常适合需要持续传输传感器数据和控制指令的'
    '机器人系统。在本课题中，Gazebo中每架无人机的里程计数据和激光雷达数据即通过Topic发布至ROS2网络，'
    '而环境封装模块中发布的速度控制指令也是以Topic的形式送达每架无人机的控制器节点。'
    '除Topic之外，ROS2还提供了基于请求-响应模型的Service通信和用于连续任务编排的Action通信。'
    '本课题利用ROS2的Service机制向Gazebo仿真器发送世界控制命令（暂停、重置、步进），实现对仿真环境运行节律的精确调控。'
