add_paragraph(
    'Sunehag等人[7]在2018年率先提出了值分解网络（Value Decomposition Networks, VDN），其基本思路是将团队总Q函数表示为'
    '各智能体个体Q函数的简单加和：Q_tot = Σ_i Q_i。VDN的分解形式极为简洁，但加性分解的假设在复杂协作场景中可能过于严格。'
    '同年，Rashid等人[8]在VDN的基础上提出了QMIX算法，通过引入超网络（HyperNetwork）和一个保证单调性的混合网络结构，'
    '将VDN的加性约束放宽为单调性约束（∂Q_tot/∂Q_i ≥ 0）。这一改进使得各智能体对团队总Q值的贡献可以是非线性的，'
    '但argmax操作仍然可以在个体层面执行，实验表明QMIX在星际争霸微操环境（SMAC）[37]中显著优于VDN。'
)

