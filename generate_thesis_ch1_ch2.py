    '（即每架无人机都可能与任何其他无人机发生交互关系）。GAT的注意力系数反映了在当前态势下，'
    '一架无人机应该以多大的"关注度"来考虑其他无人机的状态信息，从而在不依赖显式通信带宽的条件下形成高效的'
