def add_paragraph(text, bold=False, indent=True):
    """Add a body paragraph with optional indent."""
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.5
    if indent:
        p.paragraph_format.first_line_indent = Pt(24)
    else:
        p.paragraph_format.first_line_indent = Pt(0)
    run = p.add_run(text)
    run.font.name = '宋体'
    run.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
    run.font.size = Pt(12)
    run.bold = bold
    return p


def add_figure_placeholder(caption):
    """Add a figure placeholder with caption."""
    p = doc.add_paragraph()
